// JavaScript Basic Arithmetic Operations

// Declare two number variables
let num1 = 10;
let num2 = 5;

// Perform basic arithmetic operations
let sum = num1 + num2;         // Addition
let difference = num1 - num2;  // Subtraction
let product = num1 * num2;     // Multiplication
let quotient = num1 / num2;    // Division
let remainder = num1 % num2;   // Modulus (remainder)

// Exponentiation operation
let num3 = num1 ** num2; // num1 raised to the power of num2

// Print results to the console
console.log("Basic Arithmetic Operations:");
console.log("----------------------------");
console.log(`Sum: ${num1} + ${num2} = ${sum}`);
console.log(`Difference: ${num1} - ${num2} = ${difference}`);
console.log(`Product: ${num1} ร ${num2} = ${product}`);
console.log(`Quotient: ${num1} รท ${num2} = ${quotient}`);
console.log(`Remainder: ${num1} % ${num2} = ${remainder}`);
console.log(`Exponentiation: ${num1} ** ${num2} = ${num3}`);
